import express from 'express';
import { createProxyMiddleware } from 'http-proxy-middleware';
import path from 'path';
import { createCellsRouter } from './routes/cells';

export const serve = (
  port: number,
  filename: string,
  dir: string,
  useProxy: boolean
) => {
  const app = express();

  app.use(createCellsRouter(filename, dir));
  if (useProxy) {
    //For launching from npm start/development purposes
    app.use(
      createProxyMiddleware({
        target: 'http://localhost:3000',
        ws: true,
        logLevel: 'silent',
      })
    );
  } else {
    //***For Launching from CLI on a user's machine ***//
    //applying path resolution algorithm to figure out absolute path to the below file
    const packagePath = require.resolve(
      '@coderaider/local-client/build/index.html'
    );
    //gives us path to build folder
    app.use(express.static(path.dirname(packagePath)));
  }

  return new Promise<void>((resolve, reject) => {
    app.listen(port, resolve).on('error', reject);
  });
};
